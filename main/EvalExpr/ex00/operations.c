int	add(int a, int b)
{
	return (a + b);
}

int	substr(int a, int b)
{
	return (a - b);
}

int	mult(int a, int b)
{
	return (a * b);
}

int	divis(int a, int b)
{
	return (a / b);
}

int	mod(int a, int b)
{
	return (a % b);
}
